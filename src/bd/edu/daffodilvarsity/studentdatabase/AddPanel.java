    //Universal ID search method
    private int searchByID(String id) {
        for(Student eachStudent: this.dataLoader.getStudents()) {
            if(eachStudent.getIdNum().equals(id)) {
                return this.dataLoader.getStudents().indexOf(eachStudent);
            }
        }
        return -1;
    }
    
